# Để chạy chương trình, đặt code vào 
# trong `hello-world.go` và sử dụng 
# câu lệnh `go run`.
$ go run hello-world.go
hello world

# Đôi khi chúng ta muốn build 
# chương trình thành tập tin nhị phân
# Chúng ta có thể làm bằng cách 
# sử dụng câu lệnh `go build`.
\
$ go build hello-world.go
$ ls
hello-world	hello-world.go

# Khi đó chúng ta có thể thực thi 
# (sử dụng) tập tin nhị phân đó trực tiếp
$ ./hello-world
hello world

# Bây giờ chúng ta có thể chạy và 
# build chương trình Go đơn giản
# Hãy cùng nhau học thêm về ngôn ngữ này nhé.